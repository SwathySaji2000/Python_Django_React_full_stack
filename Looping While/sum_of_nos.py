  # we need to print the sum of numbers by a range


start = int(input("Enter a value : "))   # i


end = int(input("Enter a value :"))

sum = 0  # temp variable we need to store sum 

while(start <= end):

    sum = sum + start  #  adding by starting to ending values   1+ 2+3+++++++++++++  so .... on

    start = start + 1  # i = i + 1

print(sum)

