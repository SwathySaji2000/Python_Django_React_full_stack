#wap to find the number is positive,negative or zero

num1 = int(input("Enter a number: "))

if num1 > 0:

    print(f"{num1} is positive")

elif num1 == 0:    #0 == 0 is true  ,elif is the condition b/wn if & else n number of elif condition can be executed

    print(f"{num1} is zero")   

else:

    print(f"{num1} is negative")     

