number1 = {1,2,3,4,5,6,7,8}

number2 = {1,2,9,0,12,13}



## union     returns the combined elements of 2 sets


print(number1.union(number2))


## intersection   returns the  common elements of 2 sets


print(number1.intersection(number2))

## Difference

print(number1.difference(number2))  #  returns the remaining elements of number1 and which removes the common element of number1 and number2.

print(number2.difference(number1)) 


